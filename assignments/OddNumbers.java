package week1.assignments;

public class OddNumbers {

	public static void main(String[] args) {
		
		
		
		
		for (int i = 1; i <= 20; i++) {
			
			if(i % 2 == 1) {
				System.out.println("odd: "+i);
			}
			else {
				System.out.println("even: "+i);
			}
			
			
			
		}
		

	}

}
