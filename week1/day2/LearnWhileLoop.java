package week1.day2;

public class LearnWhileLoop {

	public static void main(String[] args) {
		
		int input = 15;
		
		
		
		/*
		 * while(input < 10) {
		 * 
		 * System.out.println(input);
		 * 
		 * input++;
		 * 
		 * }
		 */
		 
		 

		
		do {
			
			System.out.println(input);
			
		}while(input < 10);
		
		
	}

}
